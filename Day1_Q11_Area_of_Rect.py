# Develop a Python script to calculate the area of a rectangle. Your program should take the rectangle's length and width,
# and then display the calculated area.

len = float(input("Enter lenght of the rectangle: ")) # taking input from user for length and converting it to float type
wid = float(input("Enter width of the rectangle: ")) # taking input from user for length and converting it to float type
Area_of_Rec = len * wid
print ("The area of given rectangle= ", Area_of_Rec) #Displaying the area using print statement