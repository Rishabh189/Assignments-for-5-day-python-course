# Write a Python program that converts temperatures from Celsius to Fahrenheit. 
# Use the formula (Celsius * 9/5) + 32 to perform the conversion.

cel = float(input("Enter the temperature in Celsius: ")) # Taking input from user for Celsius
fah = (cel * 9/5) + 32 #Using the formula to convert temp into fahrenheit
print ("Value of temperature in fahrenheit is: ", fah) # Printing the output in fahrenheit